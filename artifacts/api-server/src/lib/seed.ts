export async function seedFoodsIfEmpty(): Promise<void> {
  const [{ count }] = await db
    .select({ count: sql<number>`count(*)::int` })
    .from(foodsTable);

  if (count >= 10) {
    return;
  }

  const batchSize = 20;
  for (let i = 0; i < SEED_FOODS.length; i += batchSize) {
    const batch = SEED_FOODS.slice(i, i + batchSize);
    await db.insert(foodsTable).values(batch);
  }

  console.log(`[seed] Inserted ${SEED_FOODS.length} foods into database`);
}
